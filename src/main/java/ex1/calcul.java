package ex1;
/**
 * Classe calculer possédant un méthode additionner et supprimer
 * @author Alexis Darcy
 *
 */
public class calcul {
	/**
	 * methode permetant d'additionner deux chiffre
	 * @param a
	 * @param b
	 * @return resultat
	 */
	public int additionner(int a, int b){
		return a+b;
	}
	/**
	 * methode permetant de soutraire deux chiffre
	 * @param a
	 * @param b
	 * @return resultat
	 */
	public int supprimer(int a, int b){
		return a-b;
	}
}
